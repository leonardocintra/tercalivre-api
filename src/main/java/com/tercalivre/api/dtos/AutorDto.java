package com.tercalivre.api.dtos;

public class AutorDto {

	private Long id;
	private String nome;

	public AutorDto() {

	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
